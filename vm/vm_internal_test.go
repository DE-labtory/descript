package vm

import (
	"bytes"
	"testing"
)

func TestCallFuncArguments(t *testing.T) {
	tests := []struct {
		n        int
		args     []byte
		expected []byte
	}{
		{
			n: 0,
			args: []byte{
				0x00, // 0x00 pointer
				0x00, // 0x01 pointer
				0x00, // 0x02 pointer
				0x00, // 0x03 pointer
				0x00, // 0x04 pointer
				0x00, // 0x05 pointer
				0x00, // 0x06 pointer
				0x08, // 0x07 pointer

				0x00, // 0x08 size
				0x00, // 0x09 size
				0x00, // 0x0a size
				0x00, // 0x0b size
				0x00, // 0x0c size
				0x00, // 0x0d size
				0x00, // 0x0e size
				0x01, // 0x0f size
				0x0a, // 0x10 value
			},
			expected: []byte{0x0a},
		},
		{
			n: 1,
			args: []byte{
				0x00, // 0x00 pointer
				0x00, // 0x01 pointer
				0x00, // 0x02 pointer
				0x00, // 0x03 pointer
				0x00, // 0x04 pointer
				0x00, // 0x05 pointer
				0x00, // 0x06 pointer
				0x10, // 0x07 pointer

				0x00, // 0x08 pointer
				0x00, // 0x09 pointer
				0x00, // 0x0a pointer
				0x00, // 0x0b pointer
				0x00, // 0x0c pointer
				0x00, // 0x0d pointer
				0x00, // 0x0e pointer
				0x19, // 0x0f pointer

				0x00, // 0x10 size
				0x00, // 0x11 size
				0x00, // 0x12 size
				0x00, // 0x13 size
				0x00, // 0x14 size
				0x00, // 0x15 size
				0x00, // 0x16 size
				0x01, // 0x17 size
				0x0a, // 0x18 value

				0x00, // 0x19 size
				0x00, // 0x1a size
				0x00, // 0x1b size
				0x00, // 0x1c size
				0x00, // 0x1d size
				0x00, // 0x1e size
				0x00, // 0x0f size
				0x01, // 0x20 size
				0x0b, // 0x21 value
			},
			expected: []byte{0x0b},
		},
		{
			n: 2,
			args: []byte{
				0x00, // 0x00 pointer
				0x00, // 0x01 pointer
				0x00, // 0x02 pointer
				0x00, // 0x03 pointer
				0x00, // 0x04 pointer
				0x00, // 0x05 pointer
				0x00, // 0x06 pointer
				0x18, // 0x07 pointer

				0x00, // 0x08 pointer
				0x00, // 0x09 pointer
				0x00, // 0x0a pointer
				0x00, // 0x0b pointer
				0x00, // 0x0c pointer
				0x00, // 0x0d pointer
				0x00, // 0x0e pointer
				0x21, // 0x0f pointer

				0x00, // 0x10 pointer
				0x00, // 0x11 pointer
				0x00, // 0x12 pointer
				0x00, // 0x13 pointer
				0x00, // 0x14 pointer
				0x00, // 0x15 pointer
				0x00, // 0x16 pointer
				0x2a, // 0x17 pointer

				0x00, // 0x18 size
				0x00, // 0x19 size
				0x00, // 0x1a size
				0x00, // 0x1b size
				0x00, // 0x1c size
				0x00, // 0x1d size
				0x00, // 0x1e size
				0x01, // 0x1f size
				0x0a, // 0x20 value

				0x00, // 0x21 size
				0x00, // 0x22 size
				0x00, // 0x23 size
				0x00, // 0x24 size
				0x00, // 0x25 size
				0x00, // 0x26 size
				0x00, // 0x27 size
				0x01, // 0x28 size
				0x0b, // 0x29 value

				0x00, // 0x2a size
				0x00, // 0x2b size
				0x00, // 0x2c size
				0x00, // 0x2d size
				0x00, // 0x2e size
				0x00, // 0x2f size
				0x00, // 0x30 size
				0x01, // 0x31 size
				0x0c, // 0x32 value
			},
			expected: []byte{0x0c},
		},
		// test when size is higher than 1
		{
			n: 0,
			args: []byte{
				0x00, // 0x00 pointer
				0x00, // 0x01 pointer
				0x00, // 0x02 pointer
				0x00, // 0x03 pointer
				0x00, // 0x04 pointer
				0x00, // 0x05 pointer
				0x00, // 0x06 pointer
				0x08, // 0x07 pointer

				0x00, // 0x08 size
				0x00, // 0x09 size
				0x00, // 0x0a size
				0x00, // 0x0b size
				0x00, // 0x0c size
				0x00, // 0x0d size
				0x00, // 0x0e size
				0x02, // 0x0f size
				0x0a, // 0x10 value
				0x0b, // 0x11 value
			},
			expected: []byte{0x0a, 0x0b},
		},
		{
			n: 1,
			args: []byte{
				0x00, // 0x00 pointer
				0x00, // 0x01 pointer
				0x00, // 0x02 pointer
				0x00, // 0x03 pointer
				0x00, // 0x04 pointer
				0x00, // 0x05 pointer
				0x00, // 0x06 pointer
				0x10, // 0x07 pointer

				0x00, // 0x08 pointer
				0x00, // 0x09 pointer
				0x00, // 0x0a pointer
				0x00, // 0x0b pointer
				0x00, // 0x0c pointer
				0x00, // 0x0d pointer
				0x00, // 0x0e pointer
				0x1a, // 0x0f pointer

				0x00, // 0x10 size
				0x00, // 0x11 size
				0x00, // 0x12 size
				0x00, // 0x13 size
				0x00, // 0x14 size
				0x00, // 0x15 size
				0x00, // 0x16 size
				0x02, // 0x17 size
				0x0a, // 0x18 value
				0x0b, // 0x19 value

				0x00, // 0x1a size
				0x00, // 0x1b size
				0x00, // 0x1c size
				0x00, // 0x1d size
				0x00, // 0x1e size
				0x00, // 0x1f size
				0x00, // 0x20 size
				0x03, // 0x21 size
				0x01, // 0x22 value
				0x02, // 0x23 value
				0x03, // 0x24 value
			},
			expected: []byte{0x01, 0x02, 0x03},
		},
	}

	for i, tt := range tests {
		cf := CallFunc{Args: tt.args}
		result := cf.arguments(tt.n)

		if !bytes.Equal(result, tt.expected) {
			t.Errorf("test[%d] - Wrong arguments returned expected=%v, got=%v",
				i, tt.expected, result)
		}
	}
}

func TestCallFuncArgumentsPanic(t *testing.T) {
	defer func() {
		if r := recover(); r == nil {
			t.Errorf("Test case did not panic")
		}
	}()

	CallFunc{}.arguments(-1)
}
